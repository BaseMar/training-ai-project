"""Stage 1 of the project: exploratory analysis of training data.

This script analyzes synthetic strength-training data generated by the
`generator/` module. The goal of this stage is to inspect the dataset structure,
data quality, variable distributions, training trends, and first features for
later modeling.

Analysis outputs, helper tables, and plots are saved to `outputs/eda_outputs`.
The final part contains a baseline regression model and a simple recommendation
prototype. These are not final system components yet; they are a bridge toward
Stage 2: modeling and training recommendations.
"""


