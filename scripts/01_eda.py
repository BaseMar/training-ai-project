# 31. Baseline ML data preparation
# ============================================================

print_section("BASELINE ML DATA PREPARATION")

model_df = (
    df
    .sort_values(["user_id", "exercise", "date", "session_id", "set_number"])
    .copy()
)

# Historical features use only earlier sets. shift(1) limits data leakage because
# the model cannot see information from the set currently being predicted.
model_df["prev_weight"] = model_df.groupby(["user_id", "exercise"])["weight"].shift(1)
model_df["prev_reps"] = model_df.groupby(["user_id", "exercise"])["reps"].shift(1)
model_df["prev_rir"] = model_df.groupby(["user_id", "exercise"])["rir"].shift(1)
model_df["prev_fatigue"] = model_df.groupby(["user_id", "exercise"])["fatigue"].shift(1)
model_df["prev_volume"] = model_df.groupby(["user_id", "exercise"])["volume"].shift(1)

model_df["rolling_weight_3"] = (
    model_df
    .groupby(["user_id", "exercise"])["weight"]
    .transform(lambda x: x.shift(1).rolling(3).mean())
)

model_df["rolling_reps_3"] = (
    model_df
    .groupby(["user_id", "exercise"])["reps"]
    .transform(lambda x: x.shift(1).rolling(3).mean())
)

model_df["rolling_volume_3"] = (
    model_df
    .groupby(["user_id", "exercise"])["volume"]
    .transform(lambda x: x.shift(1).rolling(3).mean())
)

model_df["rolling_fatigue_3"] = (
    model_df
    .groupby(["user_id", "exercise"])["fatigue"]
    .transform(lambda x: x.shift(1).rolling(3).mean())
)

model_df["rolling_rir_3"] = (
    model_df
    .groupby(["user_id", "exercise"])["rir"]
    .transform(lambda x: x.shift(1).rolling(3).mean())
)

eda_model_ready = model_df.dropna().copy()

print(f"Number of records after historical feature creation: {eda_model_ready.shape[0]:,}")

display_table(
    eda_model_ready[
        [
            "user_id", "exercise", "date", "set_number",
            "prev_weight", "prev_reps", "prev_rir", "prev_fatigue",
            "rolling_weight_3", "rolling_reps_3", "rolling_volume_3",
            "weight"
        ]
    ].head(),
    "Model-ready data"
)

eda_model_ready.to_csv(os.path.join(OUTPUT_DIR, "model_ready_dataset_sample.csv"), index=False)


