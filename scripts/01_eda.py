def display_table(obj, title=None):
    """Display a table in a notebook, or print it in the console otherwise."""
    if title:
        print("\n" + "=" * 100)
        print(title)
        print("=" * 100)

    try:
        display(obj)
    except NameError:
        print(obj)


def save_plot(filename):
    """Save the current plot to the EDA output directory."""
    path = os.path.join(OUTPUT_DIR, filename)
    plt.tight_layout()
    plt.savefig(path, dpi=150, bbox_inches="tight")
    plt.show()


